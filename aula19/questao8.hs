    | x == 2 = ("jonas", "doutor", 'm', 37)
    | x == 3 = ("joice", "mestre", 'f', 30)
    | x == 4 = ("janete", "doutor", 'f', 38)
    | x == 5 = ("jocileide", "doutor", 'f', 50)
    | otherwise = ("ninguem", "", 'x')
